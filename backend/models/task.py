"""
Task and transaction domain models.

Task is the central object in AMPS. Every service request — regardless of
category — uses this single schema. This standardization is intentional:
it allows the routing layer, auditor, and benchmark comparison to work
uniformly across all service types.

Future: migrate to SQLAlchemy ORM models with Alembic migrations.
"""

from __future__ import annotations

import uuid
from datetime import datetime
from typing import Any, Dict, List, Optional

from pydantic import BaseModel, Field

from .enums import TaskCategory, TaskStatus, AuditStatus, OutputType


# ---------------------------------------------------------------------------
# Task (core marketplace object)
# ---------------------------------------------------------------------------

class Task(BaseModel):
    """
    The universal service request schema used across all categories.

    Design note: all four service categories (financial_research,
    legal_analysis, market_intelligence, strategy_business_research) share
    this exact schema. Category-specific behavior lives in the agent layer,
    not in the data model.

    Future fields to consider: deadline, priority_level, attachments[],
    buyer_feedback, escrow_amount.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))

    # Identity
    buyer_id: str                             # FK -> BuyerProfile.id
    title: str
    description: str                          # The buyer's natural-language prompt / brief
    category: TaskCategory
    requested_output_type: OutputType

    # Lifecycle
    status: TaskStatus = TaskStatus.PENDING
    created_at: datetime = Field(default_factory=datetime.utcnow)
    updated_at: datetime = Field(default_factory=datetime.utcnow)
    completed_at: Optional[datetime] = None

    # Routing — marketplace matching results
    # shortlisted_seller_ids: ordered list from matching engine (best first)
    shortlisted_seller_ids: List[str] = Field(default_factory=list)
    selected_seller_id: Optional[str] = None   # FK -> SellerProfile.id; set on seller selection
    selected_quote_id: Optional[str] = None    # FK -> Quote.id; set when buyer accepts a quote
    quote_ids: List[str] = Field(default_factory=list)  # All quotes generated for this task
    # marketplace_run_at: timestamp when matching was last triggered
    marketplace_run_at: Optional[datetime] = None

    # Context
    context_url: Optional[str] = None         # Optional reference URL for the task
    additional_context: Optional[Dict[str, Any]] = {}  # Freeform metadata

    # Results
    seller_result: Optional[Dict[str, Any]] = None    # Specialist seller's output
    generalist_result: Optional[Dict[str, Any]] = None  # Baseline generalist's output

    # Benchmarking
    generalist_comparison_enabled: bool = True  # Whether generalist runs in parallel
    benchmark_comparison_id: Optional[str] = None  # FK -> BenchmarkComparison.id

    # Audit
    audit_status: AuditStatus = AuditStatus.NOT_STARTED
    audit_result_id: Optional[str] = None     # FK -> AuditResult.id

    class Config:
        use_enum_values = True


# ---------------------------------------------------------------------------
# Quote
# ---------------------------------------------------------------------------

class Quote(BaseModel):
    """
    A seller's price and ETA estimate for a specific task.

    For FIXED pricing sellers, quotes are auto-generated from base_price.
    For QUOTED pricing sellers, quotes are generated by the seller agent.

    Future: add expiry_at, negotiation_rounds, counter_quote support.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    task_id: str                              # FK -> Task.id
    seller_id: str                            # FK -> SellerProfile.id
    proposed_price: float                     # USD
    estimated_minutes: int
    confidence_score: float                   # Seller's self-reported confidence for this task
    notes: Optional[str] = None              # Seller's optional explanation / breakdown

    # Marketplace matching fields — added by marketplace/quoting.py
    match_score: float = 0.0                  # 0.0–1.0 composite match score from matching engine
    fit_explanation: Optional[str] = None     # Human-readable explanation of why this seller was matched
    # score_breakdown: per-dimension scores used to compute match_score
    # Keys: category_relevance, benchmark, reputation, price, confidence, capacity
    score_breakdown: Dict[str, float] = Field(default_factory=dict)
    seller_display_name: Optional[str] = None  # Denormalised for easy display without a join

    accepted: bool = False
    created_at: datetime = Field(default_factory=datetime.utcnow)


