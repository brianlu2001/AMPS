class AuditResult(BaseModel):
    """
    The auditor agent's evaluation of a completed task's output.

    Dimension scores (each 0.0–1.0):
      quality      — output depth, reasoning quality, task alignment
      relevance    — how well the output addresses the specific task description
      completeness — presence of all expected output elements for this category
      genericity   — inverted: 1.0 = highly specific, 0.0 = entirely generic

    composite_score: weighted average of all dimensions (used for pass/fail).
    quality_score:   kept for backwards compatibility; equals composite_score.

    admin_override: True if admin manually reversed the auditor's decision.
    overridden_at:  timestamp of override (None if no override).

    scoring_method: "mock_heuristic" for MVP; "llm_judge" when real LLM wired.

    Future: add buyer_feedback_score once buyer rating flow is implemented.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    task_id: str                                    # FK -> Task.id
    auditor_id: str                                 # FK -> AuditorProfile.id

    # --- Composite score (primary) ---
    composite_score: float = 0.0                    # Weighted average of dimension scores
    quality_score: float = 0.0                      # Alias for composite_score (backwards compat)
    passed: bool = False                            # composite_score >= QUALITY_THRESHOLD

    # --- Per-dimension scores ---
    dimension_scores: Dict[str, float] = Field(default_factory=dict)
    # Expected keys: "quality", "relevance", "completeness", "genericity"

    # --- Flags and narrative ---
    reasoning: str = ""                             # Auditor's explanation
    flags: List[str] = Field(default_factory=list)  # Issues: ["too_generic", "missing_risk_analysis"]
    recommendations: List[str] = Field(default_factory=list)  # Improvement suggestions

    # --- Benchmark context (populated if generalist comparison exists) ---
    has_benchmark: bool = False
    benchmark_comparison_id: Optional[str] = None  # FK -> BenchmarkComparison.id
    benchmark_winner: Optional[str] = None         # "seller" | "generalist" | "tie"
    benchmark_delta: Optional[float] = None        # seller_score - generalist_score

    # --- Admin override ---
    admin_override: bool = False
    admin_override_reason: Optional[str] = None
    overridden_at: Optional[datetime] = None
    overridden_by_user_id: Optional[str] = None

    # --- Metadata ---
    scoring_method: str = "mock_heuristic"         # "mock_heuristic" | "llm_judge"
    audited_at: datetime = Field(default_factory=datetime.utcnow)


