class Transaction(BaseModel):
    """
    Records the financial settlement for a completed task.

    For MVP, this is a record-only model (no real payment processing).
    Future: integrate Stripe or crypto escrow; add refund_id, dispute_id.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    task_id: str                              # FK -> Task.id
    buyer_id: str                             # FK -> BuyerProfile.id
    seller_id: str                            # FK -> SellerProfile.id
    quote_id: str                             # FK -> Quote.id
    amount: float                             # USD; final settled amount
    status: str = "settled"                   # Future: "pending", "refunded", "disputed"
    settled_at: datetime = Field(default_factory=datetime.utcnow)


