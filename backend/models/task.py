# Benchmark Comparison
# ---------------------------------------------------------------------------

class BenchmarkComparison(BaseModel):
    """
    Full side-by-side comparison record: specialist seller vs. generalist baseline.

    This record is the core evidence for the thesis that specialized sellers
    outperform general-purpose agents on structured professional tasks.

    Created after both seller_result and generalist_result are present on a Task.
    Stored in store.benchmark_comparisons and linked from Task.benchmark_comparison_id.

    Scoring dimensions (each 0.0–1.0, scored independently):
      - quality:       output quality vs. task brief (depth, accuracy, relevance)
      - structure:     output format match to requested_output_type
      - specificity:   domain-specific detail vs. generic content
      - completeness:  all required elements present

    recommendation: human-readable conclusion for the admin/buyer console.
      "use_specialist"   — specialist clearly better; specialization adds value
      "use_generalist"   — generalist competitive; consider cost savings
      "tie"              — no significant difference; either is acceptable
      "insufficient_data" — scores too close to draw conclusion

    Future: feed winner/delta into seller.reputation_score and
    generalist.benchmark_score automatically after each comparison.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    task_id: str                                    # FK -> Task.id
    task_category: Optional[str] = None            # Denormalised for filtering
    seller_id: str                                  # FK -> SellerProfile.id
    seller_display_name: Optional[str] = None       # Denormalised
    generalist_id: str                              # FK -> GeneralistProfile.id
    generalist_model: Optional[str] = None          # Denormalised model identifier

    # ----- Quality scores (0.0–1.0) -----
    seller_score: float                             # Composite quality score
    generalist_score: float                         # Composite quality score

    # Per-dimension breakdown (placeholder values for MVP; replace with LLM judge)
    seller_dimension_scores: Dict[str, float] = Field(default_factory=dict)
    generalist_dimension_scores: Dict[str, float] = Field(default_factory=dict)

    # ----- Cost comparison -----
    specialist_cost: float = 0.0                    # USD — from accepted Quote.proposed_price
    generalist_cost: float = 0.0                    # USD — from GeneralistProfile.cost_per_task

    # ----- ETA comparison -----
    specialist_eta_minutes: int = 0                 # From SellerProfile.estimated_minutes
    generalist_eta_minutes: int = 0                 # From GeneralistProfile.estimated_minutes

    # ----- Outcome -----
    winner: str = "tie"                             # "seller" | "generalist" | "tie"
    delta: float = 0.0                              # seller_score - generalist_score
    recommendation: str = "insufficient_data"       # See docstring
    summary: Optional[str] = None                  # Human-readable explanation

    # ----- Scoring method flag -----
    # scoring_method: "mock_heuristic" for MVP; "llm_judge" when real LLM wired
    scoring_method: str = "mock_heuristic"
    mock: bool = True                               # Cleared when real scoring is wired

    created_at: datetime = Field(default_factory=datetime.utcnow)


# ---------------------------------------------------------------------------
# Activity Log / Event Log
# ---------------------------------------------------------------------------

class ActivityLog(BaseModel):
    """
    Immutable event log entry. Every significant state change or agent action
    emits an ActivityLog entry.

    This is the data source for the observability console in the frontend.

    actor_id: the user or agent that triggered the event (nullable for system events).
    entity_type / entity_id: the object the event relates to (e.g. Task, Quote).

    Future: stream these to a message bus (Kafka, Redis Streams) for real-time
    fanout. For MVP, these are stored in-memory and polled by the frontend.
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    event_type: str          # e.g. "task.created", "seller.assigned", "audit.passed"
    actor_id: Optional[str] = None            # FK -> User.id; null = system event
    actor_role: Optional[str] = None          # UserRole value for display
    entity_type: str         # "task" | "quote" | "transaction" | "audit_result" | "user"
    entity_id: str           # ID of the affected object
    message: str             # Human-readable description for the console
    metadata: Optional[Dict[str, Any]] = {}   # Arbitrary structured context
    created_at: datetime = Field(default_factory=datetime.utcnow)  # Timestamp for log feed ordering


