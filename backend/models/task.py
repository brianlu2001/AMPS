class SellerOnboardingReview(BaseModel):
    """
    Auditor readiness record created automatically when a seller registers.

    Tracks the outcome of the auditor's evaluation of a seller's onboarding
    submission — separate from task-level AuditResult records.

    review_status mirrors the seller's approval_status progression:
      queued → in_review → approved | rejected | needs_more_info

    dimension_scores: optional per-dimension breakdown for richer feedback.
    Keys are: "completeness", "expertise_credibility", "pricing_clarity",
              "category_fit", "capacity_realism"

    Future: store multiple review cycles (resubmission after rejection).
    """
    id: str = Field(default_factory=lambda: str(uuid.uuid4()))
    seller_profile_id: str                     # FK -> SellerProfile.id
    auditor_id: Optional[str] = None           # FK -> AuditorProfile.id; None = system review
    review_status: str = "queued"              # queued | in_review | approved | rejected | needs_more_info
    overall_score: Optional[float] = None      # 0.0–1.0 overall quality score
    dimension_scores: Dict[str, float] = Field(default_factory=dict)
    passed: Optional[bool] = None              # True = recommend APPROVED, False = recommend REJECTED
    issues: List[str] = Field(default_factory=list)   # Specific problems found
    recommendations: List[str] = Field(default_factory=list)  # Suggested improvements
    reasoning: Optional[str] = None            # Auditor's overall explanation
    # --- Admin override ---
    admin_override: bool = False
    admin_override_reason: Optional[str] = None
    overridden_at: Optional[datetime] = None
    overridden_by_user_id: Optional[str] = None

    # --- Auditor annotation (set when human auditor adds comments) ---
    auditor_comment: Optional[str] = None          # Free-text human notes from auditor

    created_at: datetime = Field(default_factory=datetime.utcnow)
    reviewed_at: Optional[datetime] = None         # Set when human auditor completes review
    # NOTE: no duplicate created_at — was a bug in prior version
